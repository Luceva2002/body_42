#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (i <= 0)
		return (0);
	while (i * i <= nb)
	{
		i++;
	}
	i--;
	if (i * i == nb)
		return (i);
	return (0);
}

